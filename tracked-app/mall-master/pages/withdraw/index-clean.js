function onLoad(options) {
    this.setData({
      balance_pay_pwd: wx.getStorageSync('balance_pay_pwd')
    })
}
function onShow() {
    AUTH.checkHasLogined().then(isLogined => {
      if (!isLogined) {
        AUTH.login(this)
      } else {
        this.userAmount()
      }
    })   
}
function success(res) {
          if (res.confirm) {
            wx.navigateBack({
              delta: 0,
            })
          }
}
